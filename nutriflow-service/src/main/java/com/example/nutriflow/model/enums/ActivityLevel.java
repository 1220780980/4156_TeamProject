package com.example.nutriflow.model.enums;

/**
 * Enum representing user activity levels for calorie calculations.
 */
public enum ActivityLevel {
    /** Sedentary lifestyle - little to no exercise. */
    SEDENTARY,

    /** Moderate activity - exercise 3-5 days per week. */
    MODERATE,

    /** Active lifestyle - exercise 6-7 days per week. */
    ACTIVE
}

